
// while loop
let i=1
while (i<=10) {
    console.log(`The value of i is ${i}`);
    i = i + 1
}

// Array
let myHero = ['spiderman','superman','batman'];
let myArray = 0;
while (myArray<myHero.length) {
    console.log(`Superhero is ${myHero[myArray]}`);
    myArray++
}

// do while loop
let index =5
do {
    console.log(`The value of i is ${index}`);
    index++
} while (index<=15);